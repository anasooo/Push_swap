#include "push_swap.h"

void	ft_clear_lst(t_stack_node **lst)
{
	t_stack_node	*tmp;

	if (lst)
	{
		while (*lst)
		{
			tmp = *lst;
			(*lst) = (*lst)->next;
			free(tmp);
		}
	}
}



